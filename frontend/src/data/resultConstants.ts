import type { Lang } from "../i18n/translations";

export type ReadinessBand = "low" | "medium" | "high";

export type ReadinessNarrative = {
  band: ReadinessBand;
  titleRu: string;
  titleTg: string;
  messageRu: string;
  messageTg: string;
  recommendationRu: string;
  recommendationTg: string;
};

/** Балли омодагӣ: паст −8…−2, миёна −1…+3, баланд +4…+8 */
export function readinessNarrative(score: number | null | undefined): ReadinessNarrative | null {
  if (score === null || score === undefined || Number.isNaN(score)) return null;
  const s = score;
  if (s <= -2) {
    return {
      band: "low",
      titleRu: "Низкая готовность",
      titleTg: "Омодагии паст",
      messageRu:
        "Сейчас не самое лучшее время для принятия судьбоносных решений. Ваши ответы указывают на высокий уровень стресса или усталости.",
      messageTg:
        "Ҳоло вақти мусоид барои қарорҳои муҳим нест. Ҷавобҳои шумо сатҳи баланди стресс ё хастагиро нишон медиҳанд.",
      recommendationRu:
        "Рекомендуется сначала восстановить эмоциональные силы, отдохнуть и вернуться к тесту позже. Попробуйте разобраться с тем, что вас беспокоит сейчас.",
      recommendationTg:
        "Тавсия мешавад, ки аввал эҳсосӣ барқарор кунед, истироҳат кунед ва дертар ба санҷиш баргардед. Кӯшиш кунед сабабҳои ташвишро муайян кунед.",
    };
  }
  if (s <= 3) {
    return {
      band: "medium",
      titleRu: "Средняя готовность",
      titleTg: "Омодагии миёна",
      messageRu:
        "Вы находитесь в процессе обдумывания и поиска. Есть некоторая неуверенность или усталость, но в целом вы способны к анализу.",
      messageTg:
        "Шумо дар раванди андеша ва ҷустуҷӯ ҳастед. Номуайянӣ ё хастагӣ мумкин аст, вале умуман таҳлил карда метавонед.",
      recommendationRu:
        "Будьте внимательны к деталям. Возможно, стоит взять небольшую паузу перед основным тестированием, чтобы лучше сосредоточиться.",
      recommendationTg:
        "Бо тафсилот бодиққат бошед. Шояд пеш аз санҷиши асосӣ кӯтоҳ истироҳат кунед, то беҳтар таваҷҷӯҳ кунед.",
    };
  }
  return {
    band: "high",
    titleRu: "Высокая готовность",
    titleTg: "Омодагии баланд",
    messageRu:
      "Вы находитесь в отличном состоянии для выбора карьерного пути! Ваша уверенность и спокойствие помогут сделать правильный выбор.",
    messageTg:
      "Барои интихоби касб ҳолати хуб доред! Боварӣ ва оромии шумо ба интихоби дуруст кӯмак мекунанд.",
    recommendationRu:
      "Смело приступайте к основному тесту. Ваша концентрация и понимание своих целей сейчас на высоком уровне.",
    recommendationTg:
      "Бо эътимод ба санҷиши асосӣ сар кунед. Диққат ва фаҳмиши ҳадафҳои шумо ҳоло дар сатҳи баланд аст.",
  };
}

export function formatReadinessNarrative(lang: Lang, n: ReadinessNarrative): string {
  const title = lang === "tg" ? n.titleTg : n.titleRu;
  const message = lang === "tg" ? n.messageTg : n.messageRu;
  const rec = lang === "tg" ? n.recommendationTg : n.recommendationRu;
  return `${title}\n\n${message}\n\n${rec}`;
}

